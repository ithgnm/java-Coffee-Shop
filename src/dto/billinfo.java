
package dto;

public class billinfo {
    
    private int ID;
    private int IDBill;
    private int IDDrink;
    private int count;

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public int getIDBill() {
        return IDBill;
    }

    public void setIDBill(int IDBill) {
        this.IDBill = IDBill;
    }

    public int getIDDrink() {
        return IDDrink;
    }

    public void setIDDrink(int IDDrink) {
        this.IDDrink = IDDrink;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
